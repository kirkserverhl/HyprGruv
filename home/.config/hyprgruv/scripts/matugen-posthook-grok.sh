    "diff_delete_bg": "error_container",
    "diff_delete_fg": "on_error_container",
    "diff_insert_bg": "primary_container",
    "diff_insert_fg": "on_primary_container",
    "diff_equal_fg": "on_surface_variant",
    "diff_gutter_fg": "outline_variant",
    "md_heading_h1": "primary",
    "md_heading_h2": "secondary",
    "md_heading_h3": "tertiary",
