print("# Auto-generated by matugen — do not edit manually.")
print("# Source: ~/.config/matugen/templates/grok-theme.toml")
print()
print("[colors]")
for slot, material in slots.items():
    print(f'{slot} = "{getc(colors, material)}"')
PYEOF
fi

[[ -s "$GROK_THEME" ]] || exit 0

touch "$GROK_THEME" 2>/dev/null || true