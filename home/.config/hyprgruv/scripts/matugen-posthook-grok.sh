#!/usr/bin/env bash
# matugen-posthook-grok.sh — keep ~/.grok/themes/matugen.toml in sync with matugen
#
# Grok Build 0.2.56 only ships built-in themes. This hook regenerates the custom
# theme file on every wallpaper change so it is ready when the CLI loads user themes.
# Until then, restart Grok and try: /theme matugen

set -euo pipefail

GROK_THEME="${HOME}/.grok/themes/matugen.toml"
JSON_CACHE="${HOME}/.cache/matugen/current.json"

mkdir -p "${HOME}/.grok/themes"

if [[ -f "$JSON_CACHE" ]]; then
    python3 - "$JSON_CACHE" >"$GROK_THEME" <<'PYEOF'
import sys, json

def normalize_colors(data):
    raw = data.get("colors", {})
    if isinstance(raw.get("default"), dict) and any(
        isinstance(v, dict) and "hex" in v for v in raw["default"].values()
    ):
        return raw["default"]
    out = {}
    for name, val in raw.items():
        if not isinstance(val, dict):
            continue
        if "hex" in val:
            out[name] = val
        elif isinstance(val.get("default"), dict):
            color = val["default"].get("color") or val["default"].get("hex")
            if color:
                out[name] = {"hex": color}
    return out

def getc(colors, name, fallback="#888888"):
    c = colors.get(name, {})
    return c.get("hex", fallback) if isinstance(c, dict) else fallback

data = json.loads(open(sys.argv[1], encoding="utf-8").read())
colors = normalize_colors(data)

slots = {
    "bg_base": "surface_container_lowest",
    "bg_light": "surface_container_low",
    "bg_dark": "surface_container",
